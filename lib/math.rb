def addition(num1, num2)
  equation = num1+num2
  return equation
end

def subtraction(num1, num2)
  equation = num1-num2
  return equation
end

def division(num1, num2)
  equation = num1/num2
  return equation
end

def multiplication(num1, num2)
  equation = num1*num2
  return equation
end

def modulo(num1, num2)
  equation = num1%num2
  return equation
end

def square_root(num)
return Math.sqrt(num)
end
